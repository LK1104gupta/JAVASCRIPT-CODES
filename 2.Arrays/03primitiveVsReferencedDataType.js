// Primitive Vs Referance Data Type

// Primitive Data type
let num1=8
let num2 = num1
console.log(num1)
console.log(num2)
num1++
console.log("Value after incrementing num1")
console.log(num1)
console.log(num2)


// Referenced data type
let array1 =[25,85,32,75]
let array2 = array1
console.log(array1)
console.log(array2)
array1.push(52)
console.log("After adding value to array1")
console.log(array1)
console.log(array2)
